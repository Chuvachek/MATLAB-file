% Исходные данные
U_ph = 220;     % Фазное напряжение, В
P_nom = 55000;  % Мощность, Вт
n_nom = 970;    % об/мин
r1 = 0.039;     % Ом
x1 = 0.1;       % Ом
r2 = 0.029;     % Ом
x2 = 0.135;     % Ом
E2k = 290;      % Линейная ЭДС ротора, В
m = 3;          % Число фаз

% 1. Определение синхронной частоты и числа пар полюсов
% Для n_nom = 970 ближайшее n0 = 1000 (p=3). 
% Но на вашем скрине указано n0 = 600 (p=5). Используем данные скрина:
n0 = 600; 
omega0 = (2 * pi * n0) / 60; 

% 2. Приведение параметров ротора к статору
E2ph = E2k / sqrt(3);           % Фазная ЭДС ротора
k = (0.95 * U_ph) / E2ph;       % Коэффициент трансформации
r2_prime = (k^2) * r2;          % Приведенное активное
x2_prime = (k^2) * x2;          % Приведенное реактивное
x_sc = x1 + x2_prime;           % x_кз

% 3. Параметры для формулы Клосса
a = r1 / r2_prime;
sk = r2_prime / sqrt(r1^2 + x_sc^2);
Mmax = (m * U_ph^2) / (2 * omega0 * (r1 + sqrt(r1^2 + x_sc^2)));

% 4. Расчет характеристики
s = -1:0.01:2;  % Диапазон скольжения
s(s==0) = 1e-6; % Исключаем деление на ноль

% Формула Клосса
M = (2 * Mmax * (1 + a * sk)) ./ (s./sk + sk./s + 2 * a * sk);

% 5. Построение графика (M - ось X, s - ось Y)
figure;
plot(M, s, 'LineWidth', 2);
grid on;
hold on;

% Оформление
xlabel('Момент M, Н*м');
ylabel('Скольжение s');
title('Механическая характеристика АД (M по горизонтали)');

% Добавим линии режимов
line([0 0], [-1 2], 'Color', 'k'); % Ось ординат
line([min(M) max(M)], [0 0], 'Color', 'g', 'LineStyle', '--'); % Синхронный режим (s=0)
line([min(M) max(M)], [1 1], 'Color', 'r', 'LineStyle', '--'); % Пуск (s=1)

