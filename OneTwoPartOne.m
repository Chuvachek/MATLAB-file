%% ============= РАЗДЕЛ 2. Расчёт основных величин =============

% Исходные данные:
U      = 220;            % Напряжение, В
R_ya   = 0.014;          % Сопротивление якоря, Ом
C_F_hom= 0.384 * 0.8;    % Магнитный поток, Вб
UkF_hom= 440 * 3.34 * 0.5;  % Произведение напряжения и потока, В·Вб

% Расчеты:
I_nom  = U / R_ya;                  % Номинальный ток, А
n_0    = U / C_F_hom;               % Скорость холостого хода, об/мин
M_pusk = UkF_hom / R_ya;            % Пусковой момент, Н·м

% Вывод результатов расчётов:
fprintf('========== Результаты расчетов (раздел 2) ==========\\n');
fprintf('Номинальный ток I_nom = %.0f A\\n', I_nom);
fprintf('Скорость холостого хода n_0 = %.2f об/мин\\n', n_0);
fprintf('Пусковой момент M_pusk = %.0f H*m\\n', M_pusk);
fprintf('====================================================\\n');

%% ГРАФИКИ ДЛЯ РАЗДЕЛА 2 (свой отдельный рисунок)
figure('Name','Раздел 2 – Характеристики', 'Position',[100,100,1000,400]);
subplot(1,2,1);
I_2 = linspace(0, I_nom, 200);
n_2 = n_0 - (R_ya / C_F_hom) * I_2;
plot(I_2, n_2, 'b-', 'LineWidth',2); hold on;
plot(I_nom, n_0 - (R_ya / C_F_hom)*I_nom, 'ro','MarkerSize',8);
grid on;
xlabel('Ток I, А');
ylabel('Скорость n, об/мин');
title('n = f(I) для раздела 2');
legend('Линия', 'Расчётная точка', 'Location','best');

subplot(1,2,2);
M_2 = linspace(0, M_pusk, 200);
kF_2 = C_F_hom;   % можно вывести отдельно, если нужно
nM_2 = n_0 - (R_ya / (C_F_hom * kF_2)) * M_2;
plot(M_2, nM_2, 'r-', 'LineWidth',2); hold on;
plot(M_pusk, n_0 - (R_ya / (C_F_hom * kF_2)) * M_pusk, 'bo','MarkerSize',8);
grid on;
xlabel('Момент M, Н·м');
ylabel('Скорость n, об/мин');
title('n = f(M) для раздела 2');
legend('Линия', 'Пусковой момент', 'Location','best');
