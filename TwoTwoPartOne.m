%% 5. Диаграмма для пункта 1: U = 0.8*Uном
% Номинальные параметры из предыдущего раздела
Mmax_nom = Mmax;     % максимальный момент при Uном
sk_nom   = sk;       % критическое скольжение при Uном

% Параметры при U = 0.8*Uном (из курсовой)
Mmax_08 = 1970;      % Н*м
Mnom_08 = 840;       % Н*м
sk_08   = 0.144;     % критическое скольжение
n0_08   = 1000;      % об/мин (синхронная скорость — та же по сути)

% Если n_mech ещё не посчитан, раскомментируй:
% n_mech = n0_08 * (1 - s);

% Механические характеристики по полной формуле Клосса
M_nom = (2 * Mmax_nom * (1 + a * sk_nom)) ./ (s./sk_nom + sk_nom./s + 2 * a * sk_nom);
M_08  = (2 * Mmax_08  * (1 + a * sk_08 )) ./ (s./sk_08  + sk_08 ./s + 2 * a * sk_08 );

% Скорость в точке максимального момента при 0.8Uном
n_Mmax_08 = n0_08 * (1 - sk_08);

% Диаграмма, аналогичная пункту 2
figure('Color','w','Position',[150 150 900 600]);
hold on; grid on; grid minor;

plot(M_nom, n_mech, 'LineWidth', 2.0, 'Color', [0 0.45 0.74], ...
    'DisplayName', 'M=f(n) при U_{ном}');
plot(M_08,  n_mech, 'LineWidth', 2.0, 'Color', [0.85 0.33 0.10], ...
    'DisplayName', 'M_1=f(n) при 0{,}8U_{ном}');

% Отметки характерных точек для 0.8Uном
plot(Mmax_08, n_Mmax_08, 'ro', 'MarkerFaceColor', 'r', ...
    'DisplayName', 'M_{max} при 0{,}8U_{ном}');
% Номинальная точка при 0.8Uном (по заданному моменту)
% найдем соответствующее скольжение через обратную формулу Клосса (по желанию)
% здесь просто ставим по заданным данным:
n_nom_08 = n0_08 - (n0_08 * sk_08);  % при желании можно скорректировать
plot(Mnom_08, n_nom_08, 'ks', 'MarkerFaceColor', 'k', ...
    'DisplayName', 'M_{н} при 0{,}8U_{ном}');

xlabel('Момент M, Н·м', 'FontSize', 11, 'FontWeight', 'bold');
ylabel('Скорость n, об/мин', 'FontSize', 11, 'FontWeight', 'bold');
title('Механические характеристики при U_{ном} и 0{,}8U_{ном}', 'FontSize', 12);
legend('Location','best');
line([0 0], [min(n_mech) max(n_mech)], 'Color','k','LineWidth',1); % ось M=0