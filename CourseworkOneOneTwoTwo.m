% КУРСОВАЯ РАБОТА
% Раздел 1.1 + 1.2 для двигателя Д-812 (10-й вариант)
% Естественные и искусственные электромеханические и механические характеристики

%% ОЧИЩАЕМ ПАМЯТЬ
clear;
clc;

%% ============= ИСХОДНЫЕ ДАННЫЕ ИЗ ПРИЛОЖЕНИЯ 1 (10-й вариант) =============
% Двигатель постоянного тока параллельного возбуждения Д-812

No_pp = 10;              % Номер по порядку
tip   = 'Д-812';         % Тип двигателя
P     = 38;              % Мощность, кВт
n_nom = 565;             % Номинальные обороты, об/мин
I_nom = 192;             % Номинальный ток якоря, А
R_ya  = 0.0140;          % Сопротивление якоря, Ом
J     = 7.0;             % Момент инерции, кг·м^2
U_nom = 220;             % Номинальное напряжение, В

%% ============= ПРОВЕРКА ИСХОДНЫХ ДАННЫХ =============
disp('========== ИСХОДНЫЕ ДАННЫЕ (10-й вариант) ==========');
disp(['Тип двигателя: ', tip]);
disp(['Мощность P = ', num2str(P), ' кВт']);
disp(['Обороты n_nom = ', num2str(n_nom), ' об/мин']);
disp(['Номинальный ток I_nom = ', num2str(I_nom), ' А']);
disp(['Сопротивление R_ya = ', num2str(R_ya), ' Ом']);
disp(['Напряжение U_nom = ', num2str(U_nom), ' В']);
disp('======================================');

%% ============= РАЗДЕЛ 1.1. ЕСТЕСТВЕННЫЕ ХАРАКТЕРИСТИКИ =============
disp(' ');
disp('========== РЕЗУЛЬТАТЫ РАСЧЕТОВ (1.1, естественные) ==========');

% Проверочный номинальный ток по мощности и напряжению
I_nom_calc = (P * 1000) / U_nom;    % P в кВт -> Вт

% Скорость холостого хода (n0e в методичке)
n_0 = n_nom * (U_nom / (U_nom - I_nom * R_ya));

% Электрическая постоянная cФ
c_phi = U_nom / n_0;

% Пусковой ток (при n = 0)
I_pe = U_nom / R_ya;

% Номинальная угловая скорость, рад/с
w_nom = n_nom * pi / 30;

% Номинальный момент, Н·м
M_nom = (P * 1000) / w_nom;

% Механическая постоянная kФ_ном (M = kФ * I)
kF_nom = M_nom / I_nom;

% Пусковой момент
M_pe = I_pe * kF_nom;

% Коэффициенты линейных характеристик
k_I = R_ya / c_phi;                % для n = n0 - k_I * I
k_M = R_ya / (c_phi * kF_nom);     % для n = n0 - k_M * M

% Массивы точек только до своих пусковых значений
I_nat = linspace(0, I_pe, 200);
M_nat = linspace(0, M_pe, 200);

n_from_I_nat = n_0 - k_I * I_nat;
n_from_M_nat = n_0 - k_M * M_nat;

% ГРАФИКИ ЕСТЕСТВЕННЫХ ХАРАКТЕРИСТИК
figure('Position', [100, 100, 1200, 500]);

subplot(1, 2, 1);
plot(I_nat, n_from_I_nat, 'b-', 'LineWidth', 2); hold on;
plot(I_nom, n_nom, 'ro', 'MarkerSize', 8, 'MarkerFaceColor', 'r');
grid on;
xlabel('Ток I, А');
ylabel('Скорость n, об/мин');
title('Естественная электромеханическая характеристика n = f(I)');
legend('Естественная n(I)', 'Номинальный режим', 'Location', 'best');

subplot(1, 2, 2);
plot(M_nat, n_from_M_nat, 'r-', 'LineWidth', 2); hold on;
plot(M_nom, n_nom, 'bo', 'MarkerSize', 8, 'MarkerFaceColor', 'b');
grid on;
xlabel('Момент M, Н·м');
ylabel('Скорость n, об/мин');
title('Естественная механическая характеристика n = f(M)');
legend('Естественная n(M)', 'Номинальный режим', 'Location', 'best');

% Вывод чисел
disp(['Расчетный ток I_nom_calc = ', num2str(I_nom_calc), ' А']);
disp(['Скорость холостого хода n0 = ', num2str(n_0), ' об/мин']);
disp(['Электрическая постоянная cΦ = ', num2str(c_phi), ' В·мин/об']);
disp(['Пусковой ток I_pe = ', num2str(I_pe), ' А']);
disp(['Номинальный момент M_nom = ', num2str(M_nom), ' Н·м']);
disp(['Коэффициент kФ_ном = ', num2str(kF_nom), ' Н·м/А']);
disp(['Пусковой момент M_pe = ', num2str(M_pe), ' Н·м']);
disp(['Уравнение n(I): n = ', num2str(n_0), ' - ', num2str(k_I), ' * I']);
disp(['Уравнение n(M): n = ', num2str(n_0), ' - ', num2str(k_M), ' * M']);
disp('======================================');

%% ============= РАЗДЕЛ 1.2. ИСКУССТВЕННЫЕ ХАРАКТЕРИСТИКИ =============
disp(' ');
disp('========== РАЗДЕЛ 1.2: ИСКУССТВЕННЫЕ ХАРАКТЕРИСТИКИ ==========');

% ВАЖНО:
% Ниже коэффициенты kU, kR, kPhi ЗАДАНЫ КАК В ОБРАЗЦЕ ДЛЯ 25-ГО ВАРИАНТА!
% Для своего 10-го варианта ОБЯЗАТЕЛЬНО перепиши kU_i, kR_i, kPhi_i
% по Приложению 2 (строка с твоим номером варианта).

% ----- ПРИМЕР (как в образце для варианта 25) -----
% Случай 1: Uном, Rя, Φ = 0.5 Φном
% Случай 2: Uном, 3.5 Rя, Φном
% Случай 3: 1.5 Uном, Rя, Φном

kU  = [1.0, 1.0, 1.5];     % множители по напряжению
kR  = [1.0, 3.5, 1.0];     % множители по сопротивлению
kPhi= [0.5, 1.0, 1.0];     % множители по потоку

% ----- ДЛЯ 10-ГО ВАРИАНТА -----
% Замени kU, kR, kPhi на свои коэффициенты из таблицы Приложения 2!

% Предварительные массивы для удобства
n0_art   = zeros(1,3);
Ipe_art  = zeros(1,3);
Mpe_art  = zeros(1,3);
kI_art   = zeros(1,3);
kM_art   = zeros(1,3);
cphi_art = zeros(1,3);
kF_art   = zeros(1,3);

for i = 1:3
    % Изменённые параметры для i-го случая
    U_i    = kU(i)   * U_nom;
    R_ya_i = kR(i)   * R_ya;
    cphi_i = kPhi(i) * c_phi;     % cΦ масштабируется по потоку
    kF_i   = kPhi(i) * kF_nom;    % kФ масштабируется по потоку

    % Скорость холостого хода для i-го случая
    n0_i   = U_i / cphi_i;

    % Пусковой ток и момент для i-го случая
    Ipe_i  = U_i / R_ya_i;
    Mpe_i  = kF_i * Ipe_i;

    % Коэффициенты наклона характеристик
    kI_i   = R_ya_i / cphi_i;
    kM_i   = R_ya_i / (cphi_i * kF_i);

    % Сохраняем
    n0_art(i)   = n0_i;
    Ipe_art(i)  = Ipe_i;
    Mpe_art(i)  = Mpe_i;
    kI_art(i)   = kI_i;
    kM_art(i)   = kM_i;
    cphi_art(i) = cphi_i;
    kF_art(i)   = kF_i;

    % Вывод по каждому случаю
    fprintf('\nСлучай %d:\n', i);
    fprintf('U_%d = %.3f В, R_я_%d = %.5f Ом, kΦ_%d = %.3f\n', ...
        i, U_i, i, R_ya_i, i, kPhi(i));
    fprintf('n0_%d = %.2f об/мин\n', i, n0_i);
    fprintf('Iпе_%d = %.2f А\n', i, Ipe_i);
    fprintf('Mпе_%d = %.2f Н·м\n', i, Mpe_i);
    fprintf('Уравнение n(I): n = %.2f - %.5f * I\n', n0_i, kI_i);
    fprintf('Уравнение n(M): n = %.2f - %.5f * M\n', n0_i, kM_i);
end

%% ПОСТРОЕНИЕ ЭЛЕКТРОМЕХАНИЧЕСКИХ ХАРАКТЕРИСТИК n = f(I) ДЛЯ 1.2

% Для каждой характеристики используем СВОЙ диапазон тока: [0, Iпе_i]
I1 = linspace(0, Ipe_art(1), 200);
I2 = linspace(0, Ipe_art(2), 200);
I3 = linspace(0, Ipe_art(3), 200);

nI1 = n0_art(1) - kI_art(1) * I1;
nI2 = n0_art(2) - kI_art(2) * I2;
nI3 = n0_art(3) - kI_art(3) * I3;

%% ПОСТРОЕНИЕ МЕХАНИЧЕСКИХ ХАРАКТЕРИСТИК n = f(M) ДЛЯ 1.2

M1 = linspace(0, Mpe_art(1), 200);
M2 = linspace(0, Mpe_art(2), 200);
M3 = linspace(0, Mpe_art(3), 200);

nM1 = n0_art(1) - kM_art(1) * M1;
nM2 = n0_art(2) - kM_art(2) * M2;
nM3 = n0_art(3) - kM_art(3) * M3;

%% ГРАФИКИ: СРАВНЕНИЕ ЕСТЕСТВЕННОЙ И ИСКУССТВЕННЫХ ХАРАКТЕРИСТИК

figure('Position', [150, 150, 1200, 500]);

% Электромеханические характеристики n = f(I)
subplot(1, 2, 1);
plot(I_nat, n_from_I_nat, 'k-', 'LineWidth', 2); hold on;
plot(I1, nI1, 'b--', 'LineWidth', 1.5);
plot(I2, nI2, 'r-.', 'LineWidth', 1.5);
plot(I3, nI3, 'g:',  'LineWidth', 1.5);
grid on;
xlabel('Ток I, А');
ylabel('Скорость n, об/мин');
title('Естественная и искусственные n = f(I)');
legend('Естественная', ...
       'Случай 1', ...
       'Случай 2', ...
       'Случай 3', ...
       'Location', 'best');

% Механические характеристики n = f(M)
subplot(1, 2, 2);   
plot(M_nat, n_from_M_nat, 'k-', 'LineWidth', 2); hold on;
plot(M1, nM1, 'b--', 'LineWidth', 1.5);
plot(M2, nM2, 'r-.', 'LineWidth', 1.5);
plot(M3, nM3, 'g:',  'LineWidth', 1.5);
grid on;
xlabel('Момент M, Н·м');
ylabel('Скорость n, об/мин');
title('Естественная и искусственные n = f(M)');
legend('Естественная', ...
       'Случай 1', ...
       'Случай 2', ...
       'Случай 3', ...
       'Location', 'best');
