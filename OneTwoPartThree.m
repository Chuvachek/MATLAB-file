%% ============= РАЗДЕЛ 4. Третий расчет =============
% Исходные данные:
U      = 220 * 1.2;         % Напряжение, В (330 В)
R_ya   = 0.014 * 2;             % Сопротивление цепи якоря, Ом
C_F_hom= 0.384;             % Магнитный поток, Вб
UkF_hom= 220 * 1.5 * 3.34;  % Произведение U и Ф, В·Вб

% Расчеты:
I_pusk = U / R_ya;                   % Пусковой ток, А (~23571 A)
n_0    = U / C_F_hom;                % Скорость холостого хода, об/мин (~859 об/мин)
M_pusk = UkF_hom / R_ya;            % Пусковой момент, Н·м (~787143 Н·м)

fprintf('========== Результаты расчетов (раздел 4) ==========\\n');
fprintf('Пусковой ток I_pusk = %.0f A\\n', I_pusk);
fprintf('Скорость холостого хода n_0 = %.0f об/мин\\n', n_0);
fprintf('Пусковой момент M_pusk = %.0f H*m\\n', M_pusk);
fprintf('====================================================\\n');

%% ГРАФИКИ ДЛЯ РАЗДЕЛА 4 (ИСПРАВЛЕНО!)
figure('Name','Раздел 4 – Характеристики', 'Position',[300,300,1200,450]);

% Электромеханическая характеристика n(I)
subplot(1,2,1);
I_4 = linspace(0, I_pusk, 200);
kI_4 = R_ya / C_F_hom;        % kI = R_я / cФ
n_4 = n_0 - kI_4 * I_4;
plot(I_4, n_4, 'b-', 'LineWidth',2.5); hold on;
plot(I_pusk, 0, 'ro','MarkerSize',10,'MarkerFaceColor','r');
grid on; axis tight;
xlabel('Ток I_я, А');
ylabel('Скорость n, об/мин');
title('Электромеханическая характеристика n(I) - Раздел 4');
legend('n = n_0 - k_I \cdot I_я', 'Режим пуска', 'Location','best');

% МЕХАНИЧЕСКАЯ характеристика n(M) - ПРАВИЛЬНАЯ формула!
subplot(1,2,2);
M_4 = linspace(0, M_pusk, 200);
kF_4 = UkF_hom / U;           % kФ = UkФ/U = 3.34 (постоянная)
kM_4 = R_ya / (C_F_hom * kF_4);  % kM = R_я/(cФ·kФ) - ПРАВИЛЬНО!
nM_4 = n_0 - kM_4 * M_4;
plot(M_4, nM_4, 'r-', 'LineWidth',2.5); hold on;
plot(M_pusk, 0, 'bo','MarkerSize',10,'MarkerFaceColor','b');
grid on; axis tight;
xlabel('Момент M, Н·м');
ylabel('Скорость n, об/мин');
title('Механическая характеристика n(M) - Раздел 4');
legend('n = n_0 - k_M \cdot M', 'Режим пуска', 'Location','best');
