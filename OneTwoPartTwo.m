%% ============= РАЗДЕЛ 3. Дополнительные расчеты =============

% Исходные данные:
U      = 220;                  % Напряжение, В
R_ya   = 0.14 * 3.5;          % Сопротивление цепи якоря, Ом
C_F_hom= 0.384;               % Магнитный поток, Вб
UkF_hom= 220 * 3.34;          % Произведение U и Ф, В·Вб

% Расчеты:
I_pusk = U / R_ya;                   % Пусковой ток, А (~449 А)
n_0    = U / C_F_hom;                % Скорость холостого хода, об/мин (~573 об/мин)
M_pusk = UkF_hom / R_ya;            % Пусковой момент, Н·м (~1500 Н·м)

% Вывод результатов расчета:
fprintf('========== Результаты расчетов (раздел 3) ==========\\n');
fprintf('Пусковой ток I_pusk = %.0f A\\n', I_pusk);
fprintf('Скорость холостого хода n_0 = %.0f об/мин\\n', n_0);
fprintf('Пусковой момент M_pusk = %.0f H*m\\n', M_pusk);
fprintf('====================================================\\n');

%% ГРАФИКИ ДЛЯ РАЗДЕЛА 3 (свой отдельный рисунок) - ИСПРАВЛЕНО!
figure('Name','Раздел 3 – Характеристики', 'Position',[200,200,1200,450]);
subplot(1,2,1);
I_3 = linspace(0, I_pusk, 200);
kI_3 = R_ya / C_F_hom;     % коэффициент для n(I): kI = R_я / cФ
n_3 = n_0 - kI_3 * I_3;
plot(I_3, n_3, 'b-', 'LineWidth',2.5); hold on;
plot(I_pusk, 0, 'ro','MarkerSize',10,'MarkerFaceColor','r');  % точка пуска (n=0)
grid on; axis tight;
xlabel('Ток якоря I_я, А');
ylabel('Скорость вращения n, об/мин');
title('Электромеханическая характеристика n(I) - Раздел 3');
legend('n = n_0 - k_I \cdot I_я', 'Режим пуска', 'Location','best');

subplot(1,2,2);
M_3 = linspace(0, M_pusk, 200);
kF_3 = UkF_hom / U;        % механическая постоянная kФ = UkФ / U
kM_3 = R_ya / (C_F_hom * kF_3);  % коэффициент для n(M): kM = R_я / (cФ \cdot kФ)
nM_3 = n_0 - kM_3 * M_3;
plot(M_3, nM_3, 'r-', 'LineWidth',2.5); hold on;
plot(M_pusk, 0, 'bo','MarkerSize',10,'MarkerFaceColor','b');  % точка пуска (n=0)
grid on; axis tight;
xlabel('Момент M, Н·м');
ylabel('Скорость вращения n, об/мин');
title('Механическая характеристика n(M) - Раздел 3');
legend('n = n_0 - k_M \cdot M', 'Режим пуска', 'Location','best');
