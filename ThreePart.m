%% ИСХОДНЫЕ ДАННЫЕ Д-812 (из первого кода — ПРОВЕРЕНО)
r2 = 0.0140;          % Ом (R_я — сопротивление якоря)  
P  = 38e3;            % Вт (мощность)
n_nom = 565;          % об/мин
I_nom = 192;          % А
U_nom = 220;          % В

%% РАСЧЕТ ПАРАМЕТРОВ ЕСТЕСТВЕННОЙ ХАРАКТЕРИСТИКИ (✓ СОВПАДАЕТ)
w_nom = n_nom * pi / 30;          % рад/с
M_nom = P / w_nom;                % Н·м ≈ 2028 Н·м
n_0 = n_nom * (U_nom / (U_nom - I_nom * r2));  % об/мин ≈ 593 об/мин
c_phi = U_nom / n_0;              % В·мин/об
kF_nom = M_nom / I_nom;           % Н·м/А

%% ПУСКОВЫЕ ПАРАМЕТРЫ Д-812 (ДОЛЖНЫ БЫТЬ ИЗ ВАШЕГО ВАРИАНТА!)
% В docx это для АД 1800 Н·м — для Д-812 пересчитайте:
M_p = 1.9 * M_nom;                % Пусковой момент = 1.9 M_nom
M_per = 1.04 * M_nom;             % Момент переключения (k=1.04 из docx)

%% РАСЧЕТ РЕОСТАТА (адаптировано для ДПТ)
lambda = M_p / M_per;
% Для ДПТ: полное сопротивление при пуске R_п = U_nom / I_пуск_допустимый
s_nom = r2 / (c_phi * kF_nom);
R_p = r2 * M_nom / (s_nom * M_p);       % ≈ 0.361 Ом
n = log(R_p / r2) / log(lambda);  
n = round(n);                     % Число ступеней

%% АНАЛИТИЧЕСКИЕ СОПРОТИВЛЕНИЯ СТУПЕНЕЙ
R_d_analyt = zeros(1, n);
for i = 1:n
    R_d_analyt(i) = r2 * lambda^(i-1) * (lambda - 1);
end

%% ГРАФИЧЕСКИЕ — ИЗМЕНИТЕ НА СВОИ ИЗ ДИАГРАММЫ!
R_d_graph = R_d_analyt * 1.05;    % Пример: +5% как в docx
raschod = abs(R_d_analyt - R_d_graph) ./ R_d_graph * 100;

%% ВЫВОД
fprintf('Д-812: M_nom=%.0f Н·м, n=%d ступеней\n', M_nom, n);
fprintf('lambda=%.2f, R_п=%.3f Ом\n', lambda, R_p);
fprintf('R_д аналит. (Ом): '); disp(R_d_analyt);
fprintf('R_д графич. (Ом): '); disp(R_d_graph);
fprintf('Расхождение %%: '); disp(raschod);

