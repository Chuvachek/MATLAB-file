% КУРСОВАЯ РАБОТА
% Исходные данные для двигателя Д-812

% ОЧИЩАЕМ ПАМЯТЬ (чтобы старые переменные не мешали)
clear;   % удаляет все переменные
clc;     % очищает командное окно

% ============= ИСХОДНЫЕ ДАННЫЕ ИЗ ТАБЛИЦЫ =============
% Дано:

No_pp = 10;              % Номер по порядку
tip = 'Д-812';           % Тип двигателя (текст)
P = 38;                  % Мощность, кВт
n_nom = 565;             % Номинальные обороты, об/мин
I_nom = 192;             % Номинальный ток якоря, А
R_ya = 0.0140;           % Сопротивление якоря, Ом
J = 7.0;                 % Момент инерции, кг·м²
U_nom = 220;             % Напряжение (из столбца Исполнение), В

% ============= ПРОВЕРЯЕМ, ЧТО ВВЕЛИ =============
disp('========== ИСХОДНЫЕ ДАННЫЕ ==========');
disp(['Тип двигателя: ', tip]);
disp(['Мощность P = ', num2str(P), ' кВт']);
disp(['Обороты n = ', num2str(n_nom), ' об/мин']);
disp(['Ток I = ', num2str(I_nom), ' А']);
disp(['Сопротивление R = ', num2str(R_ya), ' Ом']);
disp(['Напряжение U = ', num2str(U_nom), ' В']);
disp('======================================');


% ============= РАСЧЕТЫ =============
disp(' ');
disp('========== РЕЗУЛЬТАТЫ РАСЧЕТОВ ==========');


% ==страница 4==
I_nom_calc = (P * 1000) / U_nom;  % P в кВт → Вт
n_0 = n_nom * (U_nom / (U_nom - I_nom * R_ya));
c_phi = U_nom / n_0; % электрическая константа
I_pe = U_nom / R_ya; % Пусковой ток (при n = 0)

% Коэффициент kФ_ном (из соотношения M = kФ * I)
% Сначала нужно рассчитать номинальный момент M_nom
% Номинальная угловая скорость, рад/с
w_nom = n_nom * pi / 30;

M_nom = (P * 1000) / w_nom; % Номинальный момент, Н·м


kF_nom = M_nom / I_nom; % механическая константа

M_pe = I_pe * kF_nom; % Пусковой момент




% ===== Итог расчета естественных характеристик электродвигателя: =====
k_I = R_ya / c_phi;
k_M = R_ya / (c_phi * kF_nom);









% ============= Построение естественных характеристик электродвигателя: =============

% Создаем массивы точек для построения
I_points = linspace(0, I_pe, 100);      % от 0 до пускового тока
M_points = linspace(0, M_pe, 100);      % от 0 до пускового момента

% Рассчитываем скорости по формулам
n_from_I = n_0 - k_I * I_points;        % электромеханическая характеристика
n_from_M = n_0 - k_M * M_points;        % механическая характеристика

% Рисуем первый график - электромеханическая характеристика
figure('Position', [100, 100, 1200, 500]);

% ============= Электромеханическая и механическая характеристики двигателя постоянного тока с параллельным возбуждением. =============

subplot(1, 2, 1);
plot(I_points, n_from_I, 'b-', 'LineWidth', 2);
hold on;
plot(I_nom, n_nom, 'ro', 'MarkerSize', 8, 'MarkerFaceColor', 'r');
grid on;
xlabel('Ток I, А');
ylabel('Скорость n, об/мин');
title('Электромеханическая характеристика n = f(I)');
legend('Естественная характеристика', 'Номинальный режим');

% Рисуем второй график - механическая характеристика
subplot(1, 2, 2);
plot(M_points, n_from_M, 'r-', 'LineWidth', 2);
hold on;
plot(M_nom, n_nom, 'bo', 'MarkerSize', 8, 'MarkerFaceColor', 'b');
grid on;
xlabel('Момент M, Н·м');
ylabel('Скорость n, об/мин');
title('Механическая характеристика n = f(M)');
legend('Естественная характеристика', 'Номинальный режим');

% ============= КОНЕЦ КОДА ДЛЯ ГРАФИКОВ =============







% ============= ВЫВОД =============


% Вывод результатов
disp(['Пусковой момент: M_пe = ', num2str(M_pe), ' Н·м']);
disp(['Расчетный ток I = ', num2str(I_nom_calc), ' А']);
disp(['Скорость холостого хода n0 = ', num2str(n_0), ' об/мин']);
disp(['Электрическая постоянная cΦ = ', num2str(c_phi), ' В·мин/об']);
disp(['Пусковой ток: I_пe = ', num2str(I_pe), ' А']);
disp(['Коэффициент kФ_ном = ', num2str(kF_nom), ' Н·м/А']);
disp(['Пусковой момент: M_пe = ', num2str(M_pe), ' Н·м']);
% Дополнительно: соотношение пусковых и номинальных значений
disp(['I_пe / I_ном = ', num2str(I_pe / I_nom), ' (кратность пускового тока)']);
disp(['M_пe / M_ном = ', num2str(M_pe / M_nom), ' (кратность пускового момента)']);
% ===== КОЭФФИЦИЕНТЫ ХАРАКТЕРИСТИК =====
disp(['Уравнение n = ', num2str(n_0), ' - ', num2str(k_I), ' * I']);
disp(['Уравнение n = ', num2str(n_0), ' - ', num2str(k_M), ' * M']);

disp('======================================');



% ================= Расчёт и построение искусственных характеристик электродвигателя =================

figure('Position', [100, 100, 1400, 400]);

% Общие массивы
I = linspace(0, I_pe, 100);
M = linspace(0, M_pe, 100);

%% ===== 1. Ослабление поля (Φ = 0.8 Φном) =====
c_phi1 = 0.8 * c_phi;
kF1 = 0.8 * kF_nom;

n0_1 = U_nom / c_phi1;
kI_1 = R_ya / c_phi1;
kM_1 = R_ya / (c_phi1 * kF1);

n_I1 = n0_1 - kI_1 * I;
n_M1 = n0_1 - kM_1 * M;

subplot(1,3,1);
plot(M, n_M1, 'b', 'LineWidth', 2); hold on;
plot(I*kF1, n_I1, 'r--', 'LineWidth', 2);
grid on;
title('Рис. 1.2 (Φ = 0.8Φном)');
xlabel('M, Н·м');
ylabel('n, об/мин');
legend('n=f(M)', 'n=f(I)');

%% ===== 2. Увеличение сопротивления (R = 2Rя) =====
R2 = 2 * R_ya;

n0_2 = U_nom / c_phi;
kI_2 = R2 / c_phi;
kM_2 = R2 / (c_phi * kF_nom);

n_I2 = n0_2 - kI_2 * I;
n_M2 = n0_2 - kM_2 * M;

subplot(1,3,2);
plot(M, n_M2, 'b', 'LineWidth', 2); hold on;
plot(I*kF_nom, n_I2, 'r--', 'LineWidth', 2);
grid on;
title('Рис. 1.3 (R = 2Rя)');
xlabel('M, Н·м');
ylabel('n, об/мин');
legend('n=f(M)', 'n=f(I)');

%% ===== 3. Повышение напряжения (U = 1.2Uном) =====
U3 = 1.2 * U_nom;

n0_3 = U3 / c_phi;
kI_3 = R_ya / c_phi;
kM_3 = R_ya / (c_phi * kF_nom);

n_I3 = n0_3 - kI_3 * I;
n_M3 = n0_3 - kM_3 * M;

subplot(1,3,3);
plot(M, n_M3, 'b', 'LineWidth', 2); hold on;
plot(I*kF_nom, n_I3, 'r--', 'LineWidth', 2);
grid on;
title('Рис. 1.4 (U = 1.2Uном)');
xlabel('M, Н·м');
ylabel('n, об/мин');
legend('n=f(M)', 'n=f(I)');